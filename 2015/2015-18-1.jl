function getorzero(lights, y, x)
	if x < 1 || x > 100
		return 0
	end
	if y < 1 || y > 100
		return 0
	end
	return lights[x,y] ? 1 : 0
end

function solve(input)
	input = split(input, "\n")
	lights = zeros(Bool, 100, 100)
	for y in 1:100, x in 1:100
		lights[x,y] = (input[x][y] == '#')
	end
	for _ in 1:100
		next = copy(lights)
		for y in 1:100, x in 1:100
			ncount = 0
		    for ny in -1:1, nx in -1:1
				if !(ny == 0 && nx == 0)
					ncount += getorzero(lights, ny+y, nx+x)
				end
			end
			if lights[x,y]
				next[x,y] = (ncount in [2,3])
			else
				next[x,y] = (ncount == 3)
			end
		end
		lights = copy(next)
	end
	return sum(lights)
end

output = solve(input)

println(output)
